#include <stdio.h>

int main()
{
	int num1 = 3; 
	int num2 = 5;
	int a, b, c;
	a=num1 & num2;
	//& 按位与  按二进制位
	//00000000000000000000000000000011
	//00000000000000000000000000000101
	//00000000000000000000000000000001
	b=num1 | num2;
	//& 按位或  按二进制位
	//00000000000000000000000000000011
	//00000000000000000000000000000101
	//00000000000000000000000000000111
	c=num1 ^ num2;
	//& 按位异或  按二进制位
	//00000000000000000000000000000011
	//00000000000000000000000000000101
	//00000000000000000000000000000101
	printf("%d\n", a);
	printf("%d\n", b);
	printf("%d\n", c);
	return 0;
}
